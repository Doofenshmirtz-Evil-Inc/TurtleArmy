turtle.refuel()
while turtle.detectDown() do
    
    turtle.digDown()
    turtle.forward()
    
    turtle.digDown()
    turtle.forward()
    
    turtle.digDown()
    turtle.turnLeft()

    turtle.forward()
    turtle.digDown()
    turtle.turnLeft()

    turtle.forward()
    turtle.digDown()

    turtle.forward()
    turtle.digDown()

    turtle.turnRight()
    turtle.forward()
    turtle.digDown()

    turtle.turnRight()
    turtle.forward()
    turtle.digDown()
    turtle.forward()
    turtle.digDown()

    turtle.down()
    turtle.turnLeft()
    turtle.turnLeft()

end